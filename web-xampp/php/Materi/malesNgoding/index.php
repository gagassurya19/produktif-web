<?php
  // WHILE
  $xs = 1;
  while ($xs <= 10) {
    echo "Angka $xs <br>";
    $xs++;
  }

  // Menampilkan waktu
  echo date('l, d-m-y')."<br>"; // Saturday, 07-03-20
  echo date('d / M / y')."<br>"; // 07/Mar/20
  echo date('D - M / Y')."<br>"; // Sat - Mar / 2020
  echo date('h:i:s a')."<br>"; // 07:37:04 am
  echo date('l, d-m-y h:i:s a'); // Saturday, 07-03-20 07:37:57 am

  
 ?>
